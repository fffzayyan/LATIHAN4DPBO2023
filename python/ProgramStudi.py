class ProgramStudi:
    def __init__(self):
        self.nama_prodi = ""
        self.kode_prodi = ""

    def setProdi(self, nama_prodi):
        self.nama_prodi = nama_prodi

    def getProdi(self):
        return self.nama_prodi

    def setKodeP(self, kode_prodi):
        self.kode_prodi = kode_prodi

    def getKodeP(self):
        return self.kode_prodi

    